using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Less12InvarianceIEnumerable
{
    /// <summary>
    /// Base class representing an animal.
    /// </summary>
    class Animal { }

    /// <summary>
    /// Derived class representing a cat.
    /// </summary>
    class Cat : Animal { }

    /// <summary>
    /// Derived class representing a dog.
    /// </summary>
    class Dog : Animal { }

    /// <summary>
    /// Derived class representing a bird.
    /// </summary>
    class Bird : Animal { }

    /// <summary>
    /// Derived class representing a kiwi bird.
    /// </summary>
    class Kiwi : Bird { }
}
