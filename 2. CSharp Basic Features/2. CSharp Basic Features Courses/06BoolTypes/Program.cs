namespace _06BoolTypes
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // The bool type can store a logical value that takes the value true or false. 
            // These values are specified with the keywords true and false

            bool flag = true;   // assign true to the flag variable
            flag = false;       // assign false to flag.
            Console.WriteLine(flag);
        }
    }
}